#!/usr/bin/env python
from __future__ import annotations

import argparse
import csv
import json
from pathlib import Path

from common import load_yaml, resolve_workspace_path


def parse_args() -> argparse.Namespace:
    parser = argparse.ArgumentParser(
        description="Summarize post-train round2 metrics and choose selected_threshold_v2."
    )
    parser.add_argument(
        "--config",
        default="vision_uav/configs/eval_threshold_grid_hn_v2.yaml",
        help="Path to the threshold-grid evaluation YAML config.",
    )
    parser.add_argument(
        "--suite-root",
        default="vision_uav/runs/hard_negative_round2/posttrain_hn_v2",
        help="Output root generated by evaluate_hard_negative_suites.py",
    )
    return parser.parse_args()


def read_threshold_grid(path: Path) -> list[dict]:
    with path.open("r", encoding="utf-8-sig", newline="") as handle:
        return list(csv.DictReader(handle))


def suite_threshold_map(suite_root: Path, suite_name: str) -> dict[float, dict]:
    suite_summary = json.loads((suite_root / suite_name / "suite_summary.json").read_text(encoding="utf-8"))
    return {float(row["threshold"]): row for row in suite_summary["threshold_rows"]}


def main() -> int:
    args = parse_args()
    config = load_yaml(resolve_workspace_path(args.config))
    suite_root = resolve_workspace_path(args.suite_root)
    threshold_grid = read_threshold_grid(resolve_workspace_path(config["project"]) / str(config["name"]) / "threshold_grid.csv")

    baseline_test_recall = float(config["baseline_metrics"]["test"]["recall"])
    test_recall_min = float(config["acceptance"]["test_recall_min"])
    test_map_min = float(config["acceptance"]["test_map5095_min"])
    max_test_recall_drop = float(config["acceptance"]["max_test_recall_drop"])

    suite_targets = {str(item["suite_name"]): float(item["target_frame_false_positive_rate"]) for item in load_yaml(resolve_workspace_path("vision_uav/configs/hard_negative_round2_posttrain.yaml"))["suites"]}
    suite_maps = {suite_name: suite_threshold_map(suite_root, suite_name) for suite_name in suite_targets}

    candidates: list[dict] = []
    for threshold in [float(value) for value in config["thresholds"]]:
        test_row = next(row for row in threshold_grid if row["split"] == "test" and float(row["threshold"]) == threshold)
        val_row = next(row for row in threshold_grid if row["split"] == "val" and float(row["threshold"]) == threshold)
        suite_ok = True
        suite_rows = {}
        for suite_name, target in suite_targets.items():
            suite_row = suite_maps[suite_name][threshold]
            suite_rows[suite_name] = suite_row
            if float(suite_row["frame_false_positive_rate"]) > target:
                suite_ok = False
        test_recall = float(test_row["recall"])
        test_map = float(test_row["mAP50-95"])
        test_recall_drop = baseline_test_recall - test_recall
        constraints_ok = (
            suite_ok
            and test_recall >= test_recall_min
            and test_map >= test_map_min
            and test_recall_drop <= max_test_recall_drop
        )
        candidates.append(
            {
                "threshold": threshold,
                "suite_rows": suite_rows,
                "val": val_row,
                "test": test_row,
                "test_recall_drop": test_recall_drop,
                "constraints_ok": constraints_ok,
            }
        )

    selected = next((candidate for candidate in candidates if candidate["constraints_ok"]), None)
    summary = {
        "selected_threshold_v2": None if selected is None else selected["threshold"],
        "candidates": candidates,
        "recommendation": (
            "当前轮次已经满足多套件 hard negative 与 Anti-UAV test 约束。"
            if selected is not None
            else "当前轮次未满足约束，下一轮建议转入 bird rejector 两阶段方案。"
        ),
    }
    summary_path = suite_root / "hard_negative_round2_summary.json"
    summary_path.write_text(json.dumps(summary, ensure_ascii=False, indent=2) + "\n", encoding="utf-8")
    print(f"summary_json={summary_path}")
    print(json.dumps(summary, ensure_ascii=False))
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
